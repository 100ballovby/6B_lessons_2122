'''
input() - ввод с клавиатуры и сохранение в переменной
функция ВСЕГДА возвращает вам строку
'''
num1 = int(input('Введите число'))
num2 = int(input('Введите число'))

print(num1 + num2)  # сложение
print(num1 - num2)  # вычитание
print(num1 * num2)  # умножение
print(num1 / num2)  # деление 10 / 3 = 3.333333333
print(num1 ** num2)  # возведение в степень
print(num1 // num2)  # целочисленное деление 10 // 3 = 3
print(num1 % num2)  # возврат остатка от деления 10 % 3 = 1
