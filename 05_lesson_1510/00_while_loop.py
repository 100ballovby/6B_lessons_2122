x = 5

while True:  # пока Истина (бесконечно)
    print(x)  # печатать значение х
    # x = x - 1  уменьшить х и присвоить новое значение
    x -= 1
    if x == 0:  # если x в процессе уменьшения стал 0
        break  # принудительный выход из цикла

